var frstNo = +prompt("Write First number!")
var secndNo = +prompt("Write second number!")
var operation = prompt("Write operation you want to perform!")

if ( operation === "+"){
alert(frstNo + secndNo)
} 
else if ( operation === "*"){
alert(frstNo * secndNo)
} 
else if ( operation === "/"){
alert(frstNo / secndNo)
} 
else if ( operation === "%"){
alert(frstNo % secndNo)
} 